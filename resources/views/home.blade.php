            <h2>Description</h2>

            <p>
                Un panneau solaire, encore appelé module photovoltaïque, est un dispositif énergétique constitué à base de capteurs solaires thermiques ou photovoltaïques. Il destiné à convertir le rayonnement solaire en énergie thermique ou électrique.
                C’est un assemblage de cellules connectées en série pour obtenir des tensions compatibles avec les charges à alimenter. On compte en général entre 36 et 120 cellules en série par panneau. Elles sont d’une épaisseur extrêmement faible : environ 0.2 mm. Un panneau solaire dispose d’une part, d’un vitrage prévu pour résister, sauf cas particuliers, à des conditions climatiques rudes comme le vent, la grêle, la neige et d’autre part d’une face arrière qui sert à maintenir l'étanchéité des cellules pour éviter leur oxydation.
                Le tout est intégré à un cadre en aluminium pour augmenter la résistance, faciliter la manipulation et l'installation.
                Nous distinguons des panneaux solaires thermiques et des panneaux solaires photovoltaïques
            </p>

            <h2> Type de panneau solaire </h2>

            <p>
                -Les panneaux solaires thermiques :
                Ces panneaux convertissent la lumière en chaleur. Ils sont souvent utilisés dans des installations domestiques où ils sont reliés à un chauffe-eau. Le flux solaire capté par les panneaux est transformé en chaleur grâce à des capteurs solaires thermiques.
                -Les panneaux solaires photovoltaïques :
                Plus complexes, ils transforment la lumière du Soleil directement en électricité. Cette électricité est stockée dans des bactéries ou convertie par un ondulateur pour être distribué dans le réseau.
            </p>

